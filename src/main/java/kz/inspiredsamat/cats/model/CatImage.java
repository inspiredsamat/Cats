package kz.inspiredsamat.cats.model;

public class CatImage {
    private String id;
    private String url;
    private int width;
    private int height;

    public String getId() {
        return id;
    }

    public String getUrl() {
        return url;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }
}
